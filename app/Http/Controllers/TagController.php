    public function activateTag(Request $request)
    {
        if($request->has('id'))
        {
            $count = Tag::where('active','=', 1)->count();

            $tag = Tag::find($request->get('id'));
            $tag->active = ($tag->active == 0) ? 1 : 0;
            $tag->sort = $count + 1;
            $tag->save();

            return "Tag activated";
        }
    }
